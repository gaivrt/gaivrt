function suggestYongShen(question) {
  if (!question) return null;
  for (var i = 0; i < YONGSHEN_RULES.length; i++) {
    var r = YONGSHEN_RULES[i];
    for (var j = 0; j < r.kw.length; j++) {
      if (question.indexOf(r.kw[j]) !== -1) {
        return { yong: r.yong, note: r.note, matched: r.kw[j] };
      }
    }
  }
  return null;
}

